LAYOUT: dict[str, tuple[int, int, int]] = {
    "img20230723182555-1000x1000.webp": (125, 978, 6),
    "img20230723182611-1000x1000.webp": (125, 978, 6),
    "img20230723182645-1000x1000.webp": (125, 978, 6),
    "img20230723182803-1000x1000.webp": (78, 558, 6),
}


def sample_swatch(
    img: Image.Image,
    col: int,
    row: int,
    *,
    top: int,
    bottom: int,
    margin_x: int,
    cols: int = 5,
    rows: int = 24,
    swatch_x_ratio: float = 0.72,
    sample_y_ratio: float = 0.5,
) -> list[int]:
    w, h = img.size
    col_w = (w - 2 * margin_x) / cols
    row_h = (bottom - top) / rows
    x0 = margin_x + col * col_w
    y0 = top + row * row_h
    sx = int(x0 + col_w * swatch_x_ratio)
    sy = int(y0 + row_h * sample_y_ratio)
    rs = gs = bs = n = 0
    for dy in range(-3, 4):
        for dx in range(-3, 4):
            px = min(w - 1, max(0, sx + dx))
            py = min(h - 1, max(0, sy + dy))
            pr, pg, pb = img.getpixel((px, py))
            rs += pr
            gs += pg
            bs += pb
            n += 1
    return [rs // n, gs // n, bs // n]


def build_palette() -> list[dict]:
    entries: list[dict] = []
    seen: set[str] = set()

    for fname, code_cols in CODES_BY_IMAGE.items():
        top, bottom, margin_x = LAYOUT[fname]
        img = Image.open(DATA_DIR / fname).convert("RGB")
        for col, codes in enumerate(code_cols):
            for row, code in enumerate(codes):
                rgb = sample_swatch(
                    img, col, row, top=top, bottom=bottom, margin_x=margin_x
                )
                code = code.strip()
                if code in seen:
                    continue
                seen.add(code)
                entries.append({"code": code, "rgb": rgb})
    return entries


def main() -> None:
    palette = build_palette()
    out_json = DATA_DIR / "palette.json"
    out_js = ROOT / "scripts" / "colorpalette.js"

    with open(out_json, "w", encoding="utf-8") as f:
        json.dump(palette, f, indent=2)
        f.write("\n")

    lines = ["export const colorPalette = ["]
    for item in palette:
        r, g, b = item["rgb"]
        lines.append(
            f'  {{ code: "{item["code"]}", rgb: [{r}, {g}, {b}] }},'
        )
    lines.append("];")
    lines.append("")

    out_js.write_text("\n".join(lines), encoding="utf-8")

    print(f"Wrote {len(palette)} colors to {out_json}")
    print(f"Wrote {out_js}")


if __name__ == "__main__":
    main()
